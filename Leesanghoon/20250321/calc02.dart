// Method를 이용한 방법
class Calc02{
  // Property(Attribute, Field)

  // Constructor(생성자)
  
  // Method
  int addition(int num1, int num2) {
    return num1 + num2;
  }

  int subtraction(int num1, int num2) {
    return num1 - num2;
  }

  int multiplication(int num1, int num2) {
    return num1 * num2;
  }

  double division(int num1, int num2) {
    return num1 / num2;
  }
} // Calc02
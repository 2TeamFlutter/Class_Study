main(){
  // 변수 타입 연산자
  // 정수 연산자
  int intNum1 = 30;
  print(intNum1);

  int intNum2 = 20;
  print(intNum2);

  // 사칙연산
  print(" ** 정수 사칙 연산 **");
  print(intNum1 + intNum2);   // 덧셈 연산
  print(intNum1 - intNum2);   // 뺄셈 연산
  print(intNum1 * intNum2);   // 곱셈 계산
  print(intNum1 / intNum2);   // 나눗셈 계산
  print(intNum1 ~/ intNum2);  // 몫 계산
  print(intNum1 % intNum2);   // 나머지 계산

  // 실수 연산자
  double doubleNum1 = 1.5;
  double doubleNum2 = 0.2;

  print(" ** 실수 사칙 연산 **");
  print(doubleNum1 + doubleNum2);   // 덧셈 연산
  print(doubleNum1 - doubleNum2);   // 뺄셈 연산
  print(doubleNum1 * doubleNum2);   // 곱셈 계산
  print(doubleNum1 / doubleNum2);   // 나눗셈 계산
  print(doubleNum1 ~/ doubleNum2);  // 몫 계산
  print(doubleNum1 % doubleNum2);   // 나머지 계산
}